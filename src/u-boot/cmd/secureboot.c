/**
 * do_secureboot() - U-Boot command to verify a firmware image with an appended signature.
 *
 * Usage: secureboot <data_addr> <total_size>
 *  - data_addr: The memory address where ifs-rpi4.bin is loaded.
 *  - total_size: The total size (in bytes) of the file, including the 256-byte signature.
 *
 * The command computes the SHA256 hash of the payload (total_size - 256 bytes),
 * extracts the signature (the final 256 bytes), and calls the verification routine.
 */
static int do_secureboot(struct cmd_tbl *cmdtp, int flag, int argc, char * const argv[])
{
	ulong data_addr, total_size, payload_size;
	int ret;
	uint8_t hash[32];

	if (argc != 3)
		return CMD_RET_USAGE;

	data_addr = simple_strtoul(argv[1], NULL, 16);
	total_size = simple_strtoul(argv[2], NULL, 16);

	if (total_size < SIG_LEN) {
		printf("Error: Total size (0x%lx) is smaller than signature length (0x%x).\n", total_size, SIG_LEN);
		return CMD_RET_FAILURE;
	}

	payload_size = total_size - SIG_LEN;
	printf("Data address: 0x%lx, Total size: 0x%lx, Payload size: 0x%lx\n",
	       data_addr, total_size, payload_size);

	/* Compute SHA256 hash of the firmware payload */
	sha256_csum_wd((const void *)data_addr, payload_size, hash, 0);

	/* For demo purposes, the signature is embedded into the last 256 bytes of the binary */
	uint8_t *signature = (uint8_t *)(data_addr + payload_size);

	/* Call verification (our implementation in lib/lms/lms.c) */
	ret = lms_verify_signature(hash, sizeof(hash), &pubkey, signature, SIG_LEN);
	if (ret == 0) {
		printf("LMS signature verification PASSED.\n");
	} else {
		printf("LMS signature verification FAILED (ret=%d).\n", ret);
	}
	return ret;
}

U_BOOT_CMD(
	secureboot, 3, 1, do_secureboot,
	"Verify LMS signature on firmware image with appended signature",
	"<data_addr> <total_size>\n"
	"    data_addr: memory address where ifs-rpi4.bin is loaded\n"
	"    total_size: total file size (payload + 256-byte signature)"
);
